//16. Write a program to find the volume of a cylinder?

public class Q16 {
    public static void main(String[] args) {
        double radius = 5.0;
        double height = 10.0;
        double PI = 3.14;
        double volume = PI * radius * radius * height;
        System.out.println("The volume of the cylinder is: " + volume);
    }
}
